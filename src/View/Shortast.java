    public void generateDistance(){
        for(int i = 0; i < newmap.length; i++){
            for(int s = 0; s < newmap[i].length; s++){
                if(i == s){
                    newmap[i][s] = 0;
                }
                else if(i < s){
                    if(newmap[i][s] == 404){
                        continue;
                    }
                    else{
                        newmap[i][s] = ne.nextInt(46) + 5;
                    }
                }
                else{
                    continue;
                }
            }
        }
    }
    
